# Operadores Aritméticos (para realizar cálculos matemáticos)
a = 5 + 2   # Suma
print(a)
a = 5 - 2   # Resta
print(a)
a = 5 + 2   # Multiplicación
print(a)
a = 5 / 2   # División (resultado flotante)
print(a)
a = 5 // 2  # División Entera (sin decimales)
print(a)
a = 5 % 2   # Módulo (resto de la divición)
print(a)
a = 5 ** 2  # Potencia
print(a)

# Operadores de Asignación (para asignar o actualizar valores en variables)
b = 5    # Asigna el valor 5 a x
print(b)
b += 3   # Suma 3 al valor actual de x
print(b)
b -= 2   # Resta 2 al valor actual de x
print(b)
b *= 4   # Multiplica
print(b)
b /= 2   # Divide
print(b)
b %= 3   # Guarda el resto
print(b)

# Operadores de Comparación (para comparar valores. devuelven booleanos)
print(5 == 5)   # Igual a
print(5 != 3)   # Distinto de
print(5 >= 3)   # Mayor que
print(5 <= 3)   # Menor que
print(5 >= 5)   # Mayor o Igual que
print(4 <= 3)   # Menor o Igual que

# Operadores Lógicos (para combinar comparaciones)
print((5 > 2) and (3 < 4))   # Verdadero si ambas lo son
print((5 > 2) or (3 > 4))    # Verdadero si al menos una lo es
print(not (5 > 2))           # Invierte el valor lógico

# Operadores de Pertenencia (para comprobar si un valor está dentro de una secuencia, lista, texto, etc)
print("a" in "casa")        # Devuelve True si el valor está presente
print("z" not in "casa")    # Devuelve True si el valor no está presente



