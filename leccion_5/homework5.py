# 1. Cálculos Básicos
x = 11
y = 9

print(x + y)
print(x - y)
print(x * y)
print(x // y)
print(x % y)
print(x ** y)

# 2. Comparaciones
print(x > y)
print(x == y)
print(x <= y)

# 3. Operadores Combinados
edad = 25
tiene_licencia = False

print(edad >= 18 and tiene_licencia)

# 4. Extra (Opcional)
palabra = "python"
print("p" in palabra)
print("z" not in palabra)






