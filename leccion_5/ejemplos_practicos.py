# Aritméticos
a = 8
b = 3
print(a + b)   # 11
print(a ** b)  # 512

# Comparación
print(a > b)   # True
print(a == b)  # False

# Lógicos
edad = 20
tiene_licencia = True
print(edad >= 18 and tiene_licencia)   # True

